def parse_env_file(env_path: str) -> dict:
    """Parse a .env file into a dict (values only; does not modify os.environ)."""
    values = {}
    if not os.path.exists(env_path):
        return values
    try:
        with open(env_path, "r", encoding="utf-8") as f:
            for raw_line in f:
                line = raw_line.strip()
                if not line or line.startswith("#") or "=" not in line:
                    continue
                key, value = line.split("=", 1)
                key   = key.strip()
                value = value.strip().strip('"').strip("'")
                if key:
                    values[key] = value
    except Exception as e:
        warn(f"Could not parse {env_path}: {e}")
    return values


def prepare_runtime_env(project_root: str, backend_dir: str) -> dict:
    """
    Build the runtime environment for the FastAPI process:
      1. OS environment
      2. backend/.env  (lowest priority)
      3. project .env  (highest priority for local dev)
    Returns the merged dict; prints any key-conflict warnings.
    """
    runtime_env    = os.environ.copy()
    backend_env    = parse_env_file(os.path.join(backend_dir,    ".env"))
    root_env       = parse_env_file(os.path.join(project_root,   ".env"))

    # Merge in priority order (root_env wins)
    runtime_env.update(backend_env)
    runtime_env.update(root_env)

    # JWT_SECRET_KEY normalisation
    b_jwt = backend_env.get("JWT_SECRET_KEY")
    r_jwt = root_env.get("JWT_SECRET_KEY")
    if b_jwt and r_jwt and b_jwt != r_jwt:
        warn("JWT_SECRET_KEY differs between backend/.env and .env — using .env value.")

    if not runtime_env.get("JWT_SECRET_KEY") and runtime_env.get("SECRET_KEY"):
        runtime_env["JWT_SECRET_KEY"] = runtime_env["SECRET_KEY"]
        info("JWT_SECRET_KEY not set; using SECRET_KEY for this run.")

    return runtime_env


