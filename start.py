def check_gpu() -> str:
    """Return a short GPU description, or empty string if CUDA not available."""
    try:
        import torch
        if torch.cuda.is_available():
            name  = torch.cuda.get_device_name(0)
            total = torch.cuda.get_device_properties(0).total_memory // (1024 ** 2)
            return f"{name} ({total} MB VRAM)"
    except Exception:
        pass
    return ""


