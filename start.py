def is_ollama_running() -> bool:
    try:
        code, _ = http_get_json(f"{OLLAMA_URL}/api/tags", timeout=3)
        return code == 200
    except Exception:
        return False


def get_ollama_models() -> list:
    try:
        code, data = http_get_json(f"{OLLAMA_URL}/api/tags", timeout=5)
        if code == 200:
            return [m.get("name", "") for m in data.get("models", [])]
    except Exception:
        pass
    return []


def start_ollama() -> bool:
    """Start Ollama server with GPU acceleration if not already running."""
    if is_ollama_running():
        ok("Ollama already running")
        return True

    print("  Starting Ollama with GPU acceleration...")

    ollama_exe = None
    if sys.platform == "win32":
        candidates = [
            os.path.expandvars(r"%LOCALAPPDATA%\Programs\Ollama\ollama.exe"),
            os.path.expandvars(r"%PROGRAMFILES%\Ollama\ollama.exe"),
            shutil.which("ollama"),
        ]
        ollama_exe = next((p for p in candidates if p and os.path.exists(p)), None)
    else:
        ollama_exe = shutil.which("ollama")

    if not ollama_exe:
        err("Ollama not found – install from https://ollama.ai")
        warn("Chatbot and local AI summaries will be unavailable.")
        return False

    gpu_env = os.environ.copy()
    gpu_env.update({
        "OLLAMA_NUM_GPU":        "999",   # Offload all layers to GPU
        "OLLAMA_FLASH_ATTENTION": "1",    # Flash-attention for faster inference
        "OLLAMA_MAX_LOADED_MODELS": "1",  # Keep one model warm
        "OLLAMA_KEEP_ALIVE":     "15m",   # Keep model in VRAM 15 min between calls
    })

    popen_kwargs = dict(
        stdout=subprocess.DEVNULL,
        stderr=subprocess.DEVNULL,
        env=gpu_env,
    )
    if sys.platform == "win32":
        popen_kwargs["creationflags"] = subprocess.CREATE_NO_WINDOW

    try:
        subprocess.Popen([ollama_exe, "serve"], **popen_kwargs)
        for i in range(12):
            time.sleep(1)
            if is_ollama_running():
                ok("Ollama started successfully")
                return True
            print(f"  Waiting for Ollama... ({i+1}/12)", end="\r")
        warn("Ollama may not have started – check 'ollama serve' manually")
        return False
    except Exception as e:
        err(f"Failed to start Ollama: {e}")
        return False


def warmup_ollama_background():
    """After the server starts, pre-load the Ollama model into GPU VRAM."""
    time.sleep(6)  # Wait for FastAPI to finish initialising
    try:
        models = get_ollama_models()
        if not models:
            return
        # Pick a lightweight model that is actually downloaded
        preferred = ["qwen2.5:1.5b", "phi3:mini", "gemma2:2b", "llama3.2:latest"]
        model = next((m for m in preferred if m in models), models[0])
        url = f"{OLLAMA_URL}/api/generate"
        payload = json.dumps({
            "model": model,
            "prompt": "Hi",
            "stream": False,
            "options": {"num_predict": 1, "num_gpu": 999},
        }).encode()
        req = request.Request(url, data=payload, method="POST",
                              headers={"Content-Type": "application/json"})
        with request.urlopen(req, timeout=30):
            pass
        info(f"Ollama model '{model}' warmed up in GPU VRAM")
    except Exception:
        pass  # Silence – warmup is best-effort


