def build_react_frontend(project_root: str, force_clean: bool = False):
    """Build the React frontend if it is missing or outdated."""
    frontend_src  = os.path.join(project_root, "frontend-src")
    frontend_dist = os.path.join(project_root, "frontend-dist")
    dist_index    = os.path.join(frontend_dist, "index.html")
    package_json  = os.path.join(frontend_src,  "package.json")
    package_lock  = os.path.join(frontend_src,  "package-lock.json")
    node_modules  = os.path.join(frontend_src,  "node_modules")

    if not os.path.isdir(frontend_src):
        warn("frontend-src/ not found – skipping React build")
        return

    npm_exe = shutil.which("npm")
    if not npm_exe:
        warn("npm not in PATH – skipping React build")
        info("Install Node.js 18+ from https://nodejs.org")
        return

    # --clean: wipe node_modules
    if force_clean and os.path.isdir(node_modules):
        print("  Removing node_modules for clean reinstall...")
        try:
            if sys.platform == "win32":
                subprocess.run(
                    ["cmd", "/c", "rmdir", "/s", "/q", node_modules],
                    capture_output=True, timeout=120
                )
            else:
                shutil.rmtree(node_modules)
            print("  node_modules removed")
        except Exception as e:
            warn(f"Could not remove node_modules: {e}")

    # Decide if npm install is needed
    lock_stamp = os.path.join(node_modules, ".package-lock.json")
    stamp      = lock_stamp if os.path.exists(lock_stamp) else node_modules
    needs_install = not os.path.isdir(node_modules) or force_clean
    if not needs_install and os.path.exists(stamp):
        stamp_mtime = os.path.getmtime(stamp)
        for watched in (package_json, package_lock):
            if os.path.exists(watched) and os.path.getmtime(watched) > stamp_mtime:
                needs_install = True
                break

    if needs_install:
        label = "Clean installing" if force_clean else "Installing"
        print(f"  {label} npm dependencies...")
        result = subprocess.run(
            [npm_exe, "install"], cwd=frontend_src, capture_output=True, text=True
        )
        if result.returncode != 0:
            err(f"npm install failed:\n{result.stderr[-800:]}")
            return
        ok("npm install complete")

    # Decide if build is needed
    dist_mtime   = os.path.getmtime(dist_index) if os.path.exists(dist_index) else 0
    needs_build  = not os.path.exists(dist_index)

    if not needs_build:
        config_files = [
            package_json, package_lock,
            os.path.join(frontend_src, "index.html"),
            os.path.join(frontend_src, "vite.config.js"),
            os.path.join(frontend_src, "tailwind.config.js"),
            os.path.join(frontend_src, "postcss.config.js"),
        ]
        for f in config_files:
            if os.path.exists(f) and os.path.getmtime(f) > dist_mtime:
                needs_build = True
                break

    if not needs_build:
        src_dir = os.path.join(frontend_src, "src")
        for root_dir, _, files in os.walk(src_dir):
            for fname in files:
                if os.path.getmtime(os.path.join(root_dir, fname)) > dist_mtime:
                    needs_build = True
                    break
            if needs_build:
                break

    if not needs_build:
        ok("React build is up to date")
        return

    print("  Building React app (Vite 5)...")
    result = subprocess.run(
        [npm_exe, "run", "build"], cwd=frontend_src, capture_output=True, text=True
    )
    if result.returncode == 0:
        ok("React build complete  →  frontend-dist/")
        sw = os.path.join(project_root, "frontend-dist", "sw.js")
        if os.path.exists(sw):
            info("sw.js present – PWA offline mode enabled")
        else:
            warn("sw.js missing – ensure frontend-src/public/sw.js exists")
    else:
        err(f"React build failed:\n{result.stderr[-1200:]}")


