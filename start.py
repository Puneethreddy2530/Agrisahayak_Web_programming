# ─────────────────────────────────────────────────────────
def check_npm_packages(project_root: str) -> bool:
    """Verify that critical npm packages are declared in frontend-src/package.json."""
    pkg_json_path = os.path.join(project_root, "frontend-src", "package.json")
    if not os.path.exists(pkg_json_path):
        warn("frontend-src/package.json not found – skipping package check")
        return True
    try:
        with open(pkg_json_path, "r", encoding="utf-8") as f:
            pkg = json.load(f)
    except Exception as e:
        warn(f"Could not parse package.json: {e}")
        return True

    all_deps = {**pkg.get("dependencies", {}), **pkg.get("devDependencies", {})}

    # Packages added during feature development that must be present
    required = {
        "three":           "3D Globe visualisation  (Home page hero)",
        "canvas-confetti": "CropCycle timeline confetti animation",
        "html2canvas":     "Analytics export  –  screenshot to PDF/PNG",
        "jspdf":           "Analytics export  –  PDF generation",
    }

    missing = []
    for name, desc in required.items():
        if name in all_deps:
            ok(f"{name}  -  {desc}")
        else:
            warn(f"{name} not found in package.json  -  {desc}")
            missing.append(name)

    if missing:
        info("Fix: cd frontend-src && npm install " + " ".join(missing))
    return len(missing) == 0


